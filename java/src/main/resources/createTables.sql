create Table food (
    ID INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    FOOD_NAME VARCHAR(50),
    PRICe DECIMAL,
    DESCRIPTION VARCHAR(250),
    FOOD_DATE DATE,
    MENU_ID INT REFERENCES menus(id) on delete cascade
);

create Table menus (
    id INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    START_DATE DATE,
    END_DATE DATE
);